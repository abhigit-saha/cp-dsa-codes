// Debugging function
template <typename T>
void dbs(const string &str, T t)
{
    cerr << str << ':' << t << endl;
}

template <typename T, typename... S>
void dbs(const string &str, T t, S... s)
{
    size_t idx = str.find(',');
    cerr << str.substr(0, idx) << ':' << t << ',';
    dbs(str.substr(idx + 1), s...);
}

template <typename T>
void prc(T a, T b)
{
    cerr << '[';
    for (T i = a; i != b; ++i)
    {
        if (i != a)
            cerr << ',';
        cerr << *i;
    }
    cerr << ']' << endl;
}

// Binary exponentiation
int binpow(int b, int p, int mod)
{
    int ans = 1;
    b %= mod;
    while (p)
    {
        if (p & 1)
            ans = ans * b % mod;
        b = b * b % mod;
        p >>= 1;
    }
    return ans;
}
int mod = 1e9 + 7;
vector<int> sp(1001000, 2);
void precompute()
{
    for (int i = 2; i <= 1001000; i++)
    {
        sp[i] = i;
    }
    for (int i = 2; i <= 1001000; i++)
    {
        if (sp[i] == i)
        {
            for (int j = i; j < 1001000; j += i)
            {
                sp[j] = min(i, sp[j]);
            }
        }
    }
}
unordered_map<int, int> factors;
void fast_factor(int n)
{
    if (n == 1)
        return;
    factors[sp[n]]++;
    fast_factor(n / sp[n]);
}
void solve()
{
    // Your code here
    precompute();
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    map<vector<pair<int, int>>, int> mp;
    int ans = 0;
    for (int i = 0; i < n; i++)
    {
        vector<pair<int, int>> fact;
        fast_factor(arr[i]);
        for (auto [p, pow] : factors)
        {
            if (pow % 3)
                fact.push_back({p, pow % 3});
        }
        factors.clear();
        vector<pair<int, int>> checkvector;
        for (auto [p, pow] : fact)
        {
            checkvector.push_back({p, 3 - pow});
        }
        sort(checkvector.begin(), checkvector.end());
        sort(fact.begin(), fact.end());
        ans += mp[checkvector];
        mp[fact]++;
    }
    cout << ans << endl;
}

